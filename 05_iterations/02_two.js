// while loop

let index = 0
while (index <= 10) {
    console.log(`value of index is ${index}`);
    index = index + 2
}

let myArray = ["flash", "batman", "superman", "ironman", "spiderman"]

let arr = 0
while(arr < myArray.length){
    console.log(`value is ${arr}`);
    console.log(`value is ${myArray[arr]}`);
    arr++
}


// do-while loop

let score = 9

do{
    console.log(`score is ${score}`);
    score++
}
while(score <= 10)